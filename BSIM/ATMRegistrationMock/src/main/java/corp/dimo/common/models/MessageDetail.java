package corp.dimo.common.models;

public class MessageDetail {

	private String isoRequest;
	private String isoResponse;
	private String isoRequestParse;
	private String isoResponseParse;
	
	public String getIsoRequest() {
		return isoRequest;
	}
	public void setIsoRequest(String isoRequest) {
		this.isoRequest = isoRequest;
	}
	public String getIsoResponse() {
		return isoResponse;
	}
	public void setIsoResponse(String isoResponse) {
		this.isoResponse = isoResponse;
	}
	public String getIsoRequestParse() {
		return isoRequestParse;
	}
	public void setIsoRequestParse(String isoRequestParse) {
		this.isoRequestParse = isoRequestParse;
	}
	public String getIsoResponseParse() {
		return isoResponseParse;
	}
	public void setIsoResponseParse(String isoResponseParse) {
		this.isoResponseParse = isoResponseParse;
	}
}

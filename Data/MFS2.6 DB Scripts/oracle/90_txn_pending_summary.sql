CREATE TABLE txn_pending_summary (
  ID NUMBER(19,0) NOT NULL PRIMARY KEY,
  Version NUMBER(10,0) NOT NULL,
  LastUpdateTime TIMESTAMP(0) NOT NULL,
  UpdatedBy VARCHAR2(255 CHAR) NOT NULL,
  CreateTime TIMESTAMP(0) NOT NULL,
  CreatedBy VARCHAR2(255 CHAR) NOT NULL,
  SCTLID NUMBER(19,0) NOT NULL,
  CSRAction NUMBER(10,0),
  CSRActionTime TIMESTAMP(0),
  CSRUserID NUMBER(19,0),
  CSRUserName VARCHAR2(255 CHAR),
  CSRComment VARCHAR2(255 CHAR)
 );
 
CREATE SEQUENCE  txn_pending_summary_ID_SEQ MINVALUE 1 MAXVALUE 999999999999999999999999 INCREMENT BY 1  NOCYCLE ;


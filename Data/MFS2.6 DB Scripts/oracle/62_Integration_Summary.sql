CREATE TABLE integration_summary (
  ID NUMBER(19,0) NOT NULL PRIMARY KEY,
  Version NUMBER(10,0) NOT NULL,
  LastUpdateTime TIMESTAMP NOT NULL,
  UpdatedBy VARCHAR2(255) NOT NULL,
  CreateTime TIMESTAMP NOT NULL,
  CreatedBy VARCHAR2(255) NOT NULL,
  SctlId NUMBER(19,0) NOT NULL,
  IntegrationType VARCHAR2(255) DEFAULT NULL,
  ReconcilationID1 VARCHAR2(255) DEFAULT NULL,
  ReconcilationID2 VARCHAR2(255) DEFAULT NULL,
  ReconcilationID3 VARCHAR2(255) default null
);

